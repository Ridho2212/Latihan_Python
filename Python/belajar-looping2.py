# Looping while
print("Cetak Bilangan 10-20")
bil = 10
while (bil <= 20):
    print("Bilangan Ke -", bil)
    bil += 1

# Looping dari bawah ke atas
print("Cetak Bilangan 20-10")
bil = 20
while (bil >= 10):
    print("Bilangan Ke -", bil)
    bil -= 1

# Looping While Bilangan Ganjil & Genap
print("Cetak Bilangan Genap 1-10")
no = 1
while (no <= 10):
    if (no % 2 == 0):
        print("Bilangan Genap", no)
    no += 1

# Bilangan Ganjil
print("Cetak Bilangan Genap 1-10")
no = 10
while (no >= 1):
    if (no % 2 == 1):
        print("Bilangan Ganjil", no)
    no -= 1
